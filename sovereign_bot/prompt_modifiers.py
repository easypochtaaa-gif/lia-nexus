def get_modifier(mood: str, stage: str) -> str:
    """
    Safe lookup for prompt modifier.
    Falls back to neutral/stranger if mood or stage not found.

    Args:
        mood: One of aggressive, melancholic, devoted, exhausted, neutral, playful, protective
        stage: One of stranger, acquaintance, friend, confidant, partner

    Returns:
        Multi-line string for system prompt injection.
    """
    mood_mods = MOOD_STAGE_MODIFIERS.get(mood, MOOD_STAGE_MODIFIERS["neutral"])
    return mood_mods.get(stage, mood_mods.get("stranger", ""))


def list_moods() -> list[str]:
    """Return all available moods."""
    return list(MOOD_STAGE_MODIFIERS.keys())


def list_stages() -> list[str]:
    """Return all available relationship stages."""
    return ["stranger", "acquaintance", "friend", "confidant", "partner"]
